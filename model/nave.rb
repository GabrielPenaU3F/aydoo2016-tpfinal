class Nave



  def initialize

    @vida = 100;
    @masa = 100;
    @estado = 'vivo'

  end


  def get_vida

    return @vida

  end

  def get_masa

    return @masa

  end

  def set_vida vida

    @vida = vida

  end

  def set_masa masa

    @masa = masa

  end

  def get_estado

    return @estado

  end


end